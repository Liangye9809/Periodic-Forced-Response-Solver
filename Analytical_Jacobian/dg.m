function dF = dg(xt)

x1 = xt(:, 1);
x2 = xt(:, 2);
x3 = xt(:, 3);

dF(1, 1, :) = x2 .* x3;
dF(1, 2, :) = x1 .* x3;
dF(1, 3, :) = x1 .* x2;

dF(2, 1, :) = x2 .* exp(x1 + x3);
dF(2, 2, :) = exp(x1 + x3);
dF(2, 3, :) = x2 .* exp(x1 + x3);

a = (1 + x1.^2 + x2.^2  + x3.^2) .^ 2;
dF(3, 1, :) = -2 .* x1 ./ a;
dF(3, 2, :) = -2 .* x2 ./ a;
dF(3, 3, :) = -2 .* x3 ./ a;

end

